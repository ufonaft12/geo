					<h3>לקוח מרוצה זה כבר לא מספיק! </h3>
					<h4>איזו חברה תנצח בקרב השיווק בתחומה? מדוע הצרכן מעדיף לקנות בחנות מסוימת? מה עושה את ההבדל? מה הופך את הלקוח לנאמן? | מתקופה לתקופה משתנות אופנות שוק ונסיבות כלכליות, צצות מגמות חברתיות ותרבותיות חדשות המייצרות אפיוני-צריכה, והשאלות הקלאסיות הללו שבות ועולות.</h4>

					<p>בעולם המסחרי של ימינו, המתכנס ברוב התחומים למלחמות שיווק מתעצמות על נתחי שוק, הטקטיקה השכיחה היא  תחרות על  מחיר. כתוצאה מכך מתכנסים המתחרים בשוק למעמד של דמיון במחיר המוצע לצרכן, תוך טשטוש ההבדלים ביניהם. יתרה מזאת, המציאות הכלכלית הנוכחית, הביאה לכך שגם בשווקי פרימיום המתחרים "מיישרים קו" על היצעי שוק דומים. </p>
					<p>בעידן הצרכני בו אנו חיים, כאשר הזירה התחרותית מתעצמת והתחרות על ליבו וכיסו של הלקוח הופכת להיות קשה מיום ליום, סוגיית שירות הלקוחות וחווית הלקוח, הופכת להיות פרמטר מרכזי וקריטי לבידול של העסק בהשוואה למתחרים. נותן שירות שלא ישכיל להיות עם היד על הדופק, בהתייחס לשירות שהוא מעניק ללקוחותיו, עלול להפסיד לקוחות לטובת המתחרים. לכן, בקרב השיווק ינצח, או יפיק תועלת עיקרית, מי שיצליח להתבדל. במה נתבדל? בחוויה שנעניק ללקוח!</p>
					<p>מודל Shop-Wise של גיאוקרטוגרפיה מזהה שלושה רבדים עיקריים המרכיבים את חווית הלקוח:</p>
					<p>חווית המחיר: מחיר תחרותי הוא "תנאי הכרחי" אבל לא "מספיק", ונדרש גם מילוי תנאים נוספים משלימים מהיבט הלקוח, וביניהם: מדיניות תמחור ברורה, יציבה ואחידה ברמת הרשת, מחיר הוגן ללא מלכודות מחיר, יצירת Value for Money, באמצעות הקפדה על איכות, אחריות, מדיניות החזרות ופעולות משלימות, לצד אלמנטים מפתיעים כמו הצעות שדרוג משתלמות ומבצעים.</p>

					<p>חווית הצריכה: יצירת גירויים המגבירים את "תאוות הצריכה" אם באמצעות אלמנטים סביבתיים (תכנון החנות וגורמי הנראות הויזואלית, טעימות בחנות מזון, מוזיקה, ריחות) ואם באמצעות יועצי שירות ומכירה (החלת אמצעי שיתוף וקשר ייחודיים על הלקוח)</p>
					<p>החוויה האנושית: השרות שתעניק כחוויה אנושית, הוא אמצעי השיווק והמכירה הזול והאפקטיבי ביותר, שכן סיפוק הצרכים והחסכים האנושיים הקשורים בצריכה הוא המפתח לשיפור חווית הלקוח. מקצועיות נותן השירות, רמת התפקוד השירותי, היחס האישי והאנושי, היכולת לחסוך זמן עבור הלקוח, היכולת להפתיע לטובה את הלקוח, היכולת ליצור תחושת הסתמכות בקרב הלקוחות. כל אלה הם פרמטרים סובייקטיביים בעלי השפעה מהותית על יצירת לקוחות נאמנים ושגרירים של הארגון.</p>
					<p>זיהוי גורמי המפתח שהופכים את חווית הלקוח משגרתית לאיכותית צריך להתבצע באופן פרטני לכל ארגון, בהתאם למאפייני הפעילות שלו, סביבת התחרות והתרבות הארגונית</p>
					<p>קיימים מספר תנאים להצלחת מהלכים מכווני לקוח ובדגשה יצירת תרבות של "חווית לקוח" וביניהם: הרתמות של הנהלת החברה כולה, ולא של סמנכ"ל השירות או מנהל המכירות בלבד. ככל שבעלי תפקידים בכירים רבים יותר בארגון יבינו כי ההתמקדות בלקוח היא מנוע לצמיחת הארגון, כך גדלים הסיכויים להצלחה בהטמעת מהלכי שיפור שירות.</p>
					<p>על החברה לפעול בצורה אסטרטגית להבנת צרכי הלקוחות שלהם, להתייחס לשווי הלקוח (Customer Value), ולהבין כי פערים תרבותיים וחברתיים, מחייבים את הארגון להתייחס ללקוחות שונים באופן שונה, אבל לא פחות איכותי, תוך שימוש בכלי סגמנטציה ואפיון לקוחות.</p>
					<p>הלקוח אינו סטאטי בהתנהגותו ותפיסתו, ועל כן על החברה להפעיל כלי מדידה, אשר ינטרו מגמות ושינויים בתפיסות הצרכניות או בתפיסת השירות המוענק ע"י הארגון, לאורך זמן, ובהשוואה לעולם התחרות. כיווני הבדיקה וקהלי המטרה הם רבים ומגוונים, והעמקת הבדיקה מותאמת לכל ארגון עפ"י צרכיו (לקוחות קיימים, חדשים, פוטנציאליים, נוטשים, עובדים, וגם מלעיזים במדיה החברתית – על כך יפורסם מאמר נפר</p>
					<p>שיפור חווית הלקוח יתאפשר באמצעות מיקוד הפעילות של הארגון ו/או היחידות השונות בארגון מודל  (Geo-sim Service Image Model) של גיאוקרטוגרפיה מסמן עבור כל יחידה בארגון את הנושאים אשר התמקדות בהם תביא לשיפור מרבי בחוויית הלקוח, וניתן למדוד באמצעותו את מידת האפקטיביות של הטמעת תהליכי שירות והשפעתם על חווית הלקוח.</p>
					<p>בשנים האחרונות אנו עדים לפעילות של חברות מענפים שונים וביניהם: רכב, בנקאות, קופות חולים ועוד, אשר הבינו את החשיבות שביצירת חווית לקוח, וניכר כי משאבים רבים אשר הושקעו בתהליכי שירות ונקודות מגע שלהן עם לקוח, הניבו תוצאות נפלאות הן ברמה התדמיתית של החברות והן בביצועים העסקיים שלהן. לאחרונה חלק מהחברות, למרות המצב הכלכלי, ואולי בגללו, החליטו לעשות יותר, ובתקופה האחרונה אנו חשופים לשבירה של שיאים חדשים של יצירתיות ויוזמות חדשות שתכליתן שיפור נוסף בחוויית הלקוח לצורך יצירת נאמנות</p>
